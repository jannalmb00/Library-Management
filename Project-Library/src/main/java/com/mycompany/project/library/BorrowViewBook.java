    /**
     * <p>
     * Issues Book to Student.
     * </p>
     * @param evt    issue book  button clicked
     */
    private void issueBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueBookBtnActionPerformed
        // TODO add your handling code here:
        issueStatusTextArea.setText("");
        int id = Integer.parseInt( studentIDTextBox.getText());
        Student student = Controller.getInstance().library.getStudent(id);
        studentIDTextBox.setText("");
        issueBookBtn.setEnabled(false);
        if(student != null){
           String snEntered = snIssueBookTextBox.getText();
           Book book = Controller.getInstance().library.getBook(snEntered);
           if(book != null){
               boolean result = student.borrow(book);
               if(result){
                   issueStatusTextArea.setText(String.format("Student: %s succesfuly borrowed a book SN: %s Title: %s",
                           student.getName(), book.getSN(), book.getTitle()));
               }else{
                   issueStatusTextArea.setText("Error");
               }
               
           }else{
               issueStatusTextArea.setText("Book not found");
           }
        }else{
            issueStatusTextArea.setText("Student not found");
        }
        studentInfoDisplayTextArea.setText("");
    }//GEN-LAST:event_issueBookBtnActionPerformed

