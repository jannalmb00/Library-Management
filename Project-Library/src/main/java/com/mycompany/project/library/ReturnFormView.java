    private void returnBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnBtnActionPerformed
        // TODO add your handling code here:
        //get the student
        int id = Integer.parseInt(studentIDTextLabel.getText());
        Student student = Controller.getInstance().library.getStudent(id);
        
        //get the book
        String sn = snTextBox.getText();
        Book book = Controller.getInstance().library.getBook(sn);
        displayStatusLabel.setText("");
        if(book != null && student != null){
            boolean result = student.toReturn(book);
            if(result){
                displayStatusLabel.setText("Book returned successfuly");
            }else{
                displayStatusLabel.setText("Error");
            }
        }else{
             displayStatusLabel.setText("Book or Student is not found");
        }
        studentIDTextLabel.setText("");
        snTextBox.setText("");
            
    }//GEN-LAST:event_returnBtnActionPerformed

