    private void displayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayButtonActionPerformed
        // TODO add your handling code here:
        String catalog = catalogcomboBox.getSelectedItem().toString();
        System.out.println(catalog);
        
        switch (catalog) {
            case "View Catalog" -> {
               // System.out.println();
                libTextArea.setText("");
                Map<String, String> vcatalog = Controller.getInstance().library.viewCatalog();

                if(!vcatalog.isEmpty()){
                    StringBuilder mapAsString = new StringBuilder();
                    for (Map.Entry<String, String> entry : vcatalog.entrySet()) {
                        mapAsString.append(entry.getKey()).append(": ").append(entry.getValue()).append("\n");
                    }
                    libTextArea.setText(mapAsString.toString());
                }else{
                    libTextArea.setText("No record");
                }
               
             }
            case "View Issued Catalog" -> {
                 libTextArea.setText("");
                Map<String, String> icatalog = Controller.getInstance().library.viewIssuedBooks();

                if(! icatalog.isEmpty()){
                    StringBuilder mapAsString = new StringBuilder();
                    for (Map.Entry<String, String> entry : icatalog.entrySet()) {
                        mapAsString.append(entry.getKey()).append(": ").append(entry.getValue()).append("\n");
                    }
                    libTextArea.setText(mapAsString.toString());
                }else{
                    libTextArea.setText("No record");
                }
             }

            default -> throw new IllegalArgumentException("Unknown search type: " + catalog);
        }
    }//GEN-LAST:event_displayButtonActionPerformed

