    /**
     * <p>
     * Adds Book to Library.
     * </p>
     * @param evt   add button clicked
     */
    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
     if(newSNTextBox.getText().isEmpty() || newTitleTextBox.getText().isEmpty() ||
             newAuthorTextbox.getText().isEmpty() || newPublisherTextBox.getText().isEmpty()||
             newPriceTextBox.getText().isEmpty() ||
             newQuantityTextBox.getText().isEmpty() || newGenreComboBox.getSelectedItem() == null){
         statusLabel.setText("Error: All field must be filled put");
         return;
     } 
     try{
         String sn = newSNTextBox.getText();
         String title = newTitleTextBox.getText();
         String author = newAuthorTextbox.getText();
         double price = Double.parseDouble(newPriceTextBox.getText());
         String publisher =  newPublisherTextBox.getText();
         int quantity = Integer.parseInt(newQuantityTextBox.getText());
         String genre = newGenreComboBox.getSelectedItem().toString();
         int issued = 0;
         Date date = new Date();
         System.out.println(date);
         
         Book newBook = Bookfactory.createBook(sn, title, author, publisher, price, quantity, issued, date, genre);
         if(newBook != null){
             Controller.getInstance().library.addBook(newBook);
             statusLabel.setText("Book added successfully");
             clearTextBox();
         }else{
             statusLabel.setText("Failed to add");
         }
         
     }catch(NumberFormatException e){
         statusLabel.setText(e.getMessage());
     }
        
    }//GEN-LAST:event_addBtnActionPerformed

